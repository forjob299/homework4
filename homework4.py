my_string = input('Здравствуйте, user! Напишите о себе: ')
print('Количество символов в ответе user:', len(my_string))
print('Ответ user в верхнем регистре:', my_string.upper())
print('Ответ user в нижнем регистре:', my_string.lower())
print('Ответ user без пробелов:', my_string.replace(' ', ''))
print('Первый символ из ответа user:', my_string[0])
print('Последний символ из ответа user:', my_string[-1])



